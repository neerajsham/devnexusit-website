            const allCards = document.querySelectorAll('.customer-card');
            totalCards = originalCards.length;
            const gap = 20;
            cardWidth = document.querySelector('.customer-card')?.offsetWidth + gap || 220;
            const initialOffset = clonesNeeded * cardWidth;
            track.style.transform = `translateX(-${initialOffset}px)`;
            currentIndex = clonesNeeded;
        }
        
        function nextCustomerSlide() {
            if (isTransitioningCustomer) return;
            const gap = 20;
            cardWidth = document.querySelector('.customer-card')?.offsetWidth + gap || 220;
            currentIndex++;
            track.style.transition = 'transform 0.5s cubic-bezier(0.25, 0.46, 0.45, 0.94)';
            track.style.transform = `translateX(-${currentIndex * cardWidth}px)`;
            
            setTimeout(() => {
                if (currentIndex >= totalCards + cardsPerView) {
                    track.style.transition = 'none';
                    currentIndex = cardsPerView;
                    track.style.transform = `translateX(-${currentIndex * cardWidth}px)`;
                    setTimeout(() => {
                        track.style.transition = 'transform 0.5s cubic-bezier(0.25, 0.46, 0.45, 0.94)';
                    }, 50);
                }
                isTransitioningCustomer = false;
            }, 500);
            resetCustomerAuto();
        }
        
        function prevCustomerSlide() {
            if (isTransitioningCustomer) return;
            const gap = 20;
            cardWidth = document.querySelector('.customer-card')?.offsetWidth + gap || 220;
            currentIndex--;
            track.style.transition = 'transform 0.5s cubic-bezier(0.25, 0.46, 0.45, 0.94)';
            track.style.transform = `translateX(-${currentIndex * cardWidth}px)`;
            
            setTimeout(() => {
                if (currentIndex < cardsPerView) {
                    track.style.transition = 'none';
                    currentIndex = totalCards + cardsPerView - 1;
                    track.style.transform = `translateX(-${currentIndex * cardWidth}px)`;
                    setTimeout(() => {
                        track.style.transition = 'transform 0.5s cubic-bezier(0.25, 0.46, 0.45, 0.94)';
                    }, 50);
                }
                isTransitioningCustomer = false;
            }, 500);
            resetCustomerAuto();
        }
        
        function startCustomerAuto() {
            if (autoTimerCustomer) clearInterval(autoTimerCustomer);
            autoTimerCustomer = setInterval(() => {
                nextCustomerSlide();
            }, 4000);
        }
        
        function resetCustomerAuto() {
            if (autoTimerCustomer) clearInterval(autoTimerCustomer);
            autoTimerCustomer = setInterval(() => {
                nextCustomerSlide();
            }, 4000);
        }
        
        cardsPerView = getCardsPerViewCustomer();
        setTimeout(() => {
            setupCustomerSlider();
            startCustomerAuto();
        }, 100);
        
        prevBtn.addEventListener('click', () => {
            prevCustomerSlide();
            resetCustomerAuto();
        });
        
        nextBtn.addEventListener('click', () => {
            nextCustomerSlide();
            resetCustomerAuto();
        });
        
        const sliderContainer = document.querySelector('.customers-slider');
        if (sliderContainer) {
            sliderContainer.addEventListener('mouseenter', () => {
                if (autoTimerCustomer) clearInterval(autoTimerCustomer);
            });
            sliderContainer.addEventListener('mouseleave', startCustomerAuto);
        }
        
        let resizeTimerCustomer;
        window.addEventListener('resize', function() {
            clearTimeout(resizeTimerCustomer);
            resizeTimerCustomer = setTimeout(() => {
                const newPerView = getCardsPerViewCustomer();
                if (newPerView !== cardsPerView) {
                    cardsPerView = newPerView;
                    setupCustomerSlider();
                }
            }, 200);
        });
    }
})();




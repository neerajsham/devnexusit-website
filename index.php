(function() {
    const faqItems = document.querySelectorAll('.faq-item');
    faqItems.forEach(item => {
        const question = item.querySelector('.faq-question');
        const answer = item.querySelector('.faq-answer');
        const icon = item.querySelector('.faq-icon i');
        
        if (question && answer) {
            question.addEventListener('click', () => {
                const isOpen = answer.classList.contains('open');
                
                faqItems.forEach(otherItem => {
                    const otherAnswer = otherItem.querySelector('.faq-answer');
                    const otherIcon = otherItem.querySelector('.faq-icon i');
                    if (otherAnswer && otherAnswer !== answer) {
                        otherAnswer.classList.remove('open');
                        if (otherIcon) {
                            otherIcon.className = 'fas fa-plus';
                        }
                    }
                });
                
                if (!isOpen) {
                    answer.classList.add('open');
                    if (icon) icon.className = 'fas fa-minus';
                } else {
                    answer.classList.remove('open');
                    if (icon) icon.className = 'fas fa-plus';
                }
            });
        }
    });
})();

// ========== INTERSECTION OBSERVER FOR ANIMATIONS ==========
(function() {
    const observerOptions = {
        threshold: 0.3,
        rootMargin: '50px'
    };
    
    let animatedStats = {
        about: false,
        choosewhy: false
    };
    
    function animateNumbers(elements) {
        elements.forEach(stat => {
            if (!stat || stat.hasAttribute('data-animated')) return;
            
            const targetText = stat.innerText;
            const targetNumber = parseInt(targetText.replace(/[^0-9]/g, ''));
            const suffix = targetText.replace(/[0-9]/g, '');
            
            if (isNaN(targetNumber)) return;
            
            let current = 0;
            const increment = targetNumber / 50;
            const timer = setInterval(() => {
                current += increment;
                if (current >= targetNumber) {
                    stat.innerText = targetNumber + suffix;
                    clearInterval(timer);
                } else {
                    stat.innerText = Math.floor(current) + suffix;
                }
            }, 30);
            
            stat.setAttribute('data-animated', 'true');
        });
    }
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const section = entry.target;
                
                if (section.classList.contains('about-section') && !animatedStats.about) {
                    const statNumbers = document.querySelectorAll('.about-stat-number');
                    if (statNumbers.length) {
                        animateNumbers(statNumbers);
                        animatedStats.about = true;
                    }
                }
                
                if (section.classList.contains('choosewhy') && !animatedStats.choosewhy) {
                    const statNumbers = document.querySelectorAll('.info-stat-number');
                    if (statNumbers.length) {
                        animateNumbers(statNumbers);
                        animatedStats.choosewhy = true;
                    }
                }
            }
        });
    }, observerOptions);
    
    const aboutSection = document.querySelector('.about-section');
    const chooseSection = document.querySelector('.choosewhy');
    
    if (aboutSection) observer.observe(aboutSection);
    if (chooseSection) observer.observe(chooseSection);
})();

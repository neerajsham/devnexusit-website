   COLLISION-FREE PLACEMENT ENGINE
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━ */
const section    = document.getElementById('wcSection');
const sceneEl    = document.getElementById('wcScene');
const partCanvas = document.getElementById('wcCanvas');
const PAD = 6;

function getSize() {
  return { W: section.offsetWidth, H: section.offsetHeight };
}

function measureWord(text, px, rot) {
  const d = document.createElement('div');
  d.style.cssText = `position:fixed;visibility:hidden;top:-9999px;left:-9999px;`+
    `font-family:'Bebas Neue',sans-serif;font-size:${px}px;`+
    `white-space:nowrap;line-height:1;letter-spacing:0.03em;`;
  d.textContent = text;
  document.body.appendChild(d);
  const tw = d.offsetWidth, th = d.offsetHeight;
  document.body.removeChild(d);
  return (rot === 90 || rot === -90) ? { w: th, h: tw } : { w: tw, h: th };
}

function overlaps(a, placed) {
  for (const b of placed) {
    if (a.x - PAD < b.x + b.w + PAD &&
        a.x + a.w + PAD > b.x - PAD &&
        a.y - PAD < b.y + b.h + PAD &&
        a.y + a.h + PAD > b.y - PAD) return true;
  }
  return false;
}

function inEllipse(cx, cy, W, H) {
  const nx = (cx / W - 0.5) / 0.46;
  const ny = (cy / H - 0.5) / 0.44;
  return nx * nx + ny * ny <= 1;
}

function buildCloud() {
  const { W, H } = getSize();
  const scale = W / 1920;

  while (sceneEl.firstChild) sceneEl.removeChild(sceneEl.firstChild);

  const sorted = [...WORDS].sort((a, b) => b[1] - a[1]);
  const placed = [];
  const built  = [];

  for (const [text, basePx, col, rot] of sorted) {
    const px = Math.max(10, Math.round(basePx * scale));
    const { w, h } = measureWord(text, px, rot);

    for (let t = 0; t < 900; t++) {
      const angle = Math.random() * Math.PI * 2;
      const r     = Math.pow(Math.random(), 0.55);
      const cx    = W * 0.5 + r * W * 0.44 * Math.cos(angle) + (Math.random() - 0.5) * 18;
      const cy    = H * 0.5 + r * H * 0.42 * Math.sin(angle) + (Math.random() - 0.5) * 18;
      const bx = cx - w / 2, by = cy - h / 2;

      if (bx < 4 || by < 4 || bx + w > W - 4 || by + h > H - 4) continue;
      if (!inEllipse(cx, cy, W, H)) continue;
      const box = { x: bx, y: by, w, h };
      if (!overlaps(box, placed)) {
        placed.push(box);
        built.push({ text, px, col, rot, cx, cy });
        break;
      }
    }
  }

  built.forEach(({ text, px, col, rot, cx, cy }, i) => {
    const el = document.createElement('span');
    el.className = `w ${col}`;
    el.textContent = text;
    el.style.fontSize  = px + 'px';
    el.style.left      = cx + 'px';
    el.style.top       = cy + 'px';
    el.style.setProperty('--r',   rot + 'deg');
    el.style.transform = `translate(-50%,-50%) rotate(${rot}deg) scale(1)`;
    el.style.setProperty('--su',  (1.10 + Math.random() * 0.14).toFixed(3));
    el.style.setProperty('--sd',  (0.88 + Math.random() * 0.09).toFixed(3));
    el.style.setProperty('--dur', (2.6  + Math.random() * 2.2).toFixed(2) + 's');
    el.style.setProperty('--dly', (Math.random() * 2.5).toFixed(2) + 's');
    sceneEl.appendChild(el);
    setTimeout(() => el.classList.add('alive'), 500 + i * 38);
  });
}

